public class Node {
    private int data;
    private Node nextNode;

    public Node(int data){
        this.data = data;
    }

    public int getData(){
        return this.data = data;
    }
    public Node getNode(){
        return this.nextNode;
    }
    public void setData(int data){
        this.data = data;
    }
    public void setNextNode(Node nextNode){
        this.nextNode = nextNode;
    }
}
